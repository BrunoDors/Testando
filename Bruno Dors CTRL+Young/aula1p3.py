#tipos int e float
#INT > numero inteiro 
#o tipo int representa qualquer valor positivo ou negativo
#int sem sina é considerado positivo 
print(11)
print(-11)
print(0)

#float > numeros com pontos flutuantes 
#representa qualquer tipo de numero positivo ou negativo com virgula
#float sem sinal é considerado positivo 
print(1.0,-1.5)
print(7.00,0.00)

#funçao type mostra o tipo que o python infere ao valor 
print(type('joao'))
print(type(1))
print(type(-1,2),type(0,0),type('bruno'),type(0))